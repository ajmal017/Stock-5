def variable_array_to_string(var):
    a_content = str(var).split(", ")
    if a_content == "nan":
        return "[],"
    else:
        a_content = [x for x in a_content if x != "nan" and x != 'nan']
        return str(a_content) + ","


def variable_to_string(var):
    content = str(var)
    if content == "nan":
        return "'',"
    elif content in ["True", "False"]:
        return "" + content.lower() + ","
    else:
        return "'" + content + "',"


